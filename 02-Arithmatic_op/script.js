// oprators in js -> binary & unary 
// arithmatic opration + - * / , modulo(%), exponential(**),

let a = 5;
let b = 2;

console.log("a = ", a , "b = ", b);
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("a % b = ", a % b);
console.log("a ** b = ", a ** b);

// Unary Oprator 
let c = 5;
let d = 2;

//post increment 
console.log("c = ", c, "d = ", d);
console.log("c++ = ", c++);
console.log("c = ", c);

// pre increament
console.log("c = ", c, "d = ", d);
console.log("++c = ", ++c);
console.log("c = ", c);


// decrement
console.log("c = ", c, "d = ", d);
console.log("c-- = ", c--);
console.log("c = ", c);

